using System.Collections.Generic;

namespace Vision.Flow.Core.Domain.Flows
{
    /// <summary>
    /// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝอผืดฬฌ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฬฌ๏ฟฝฤผ๏ฟฝ๏ฟฝ๏ฟฝ
    /// </summary>
    public sealed class FlowViewState
    {
        public const double DefaultCanvasWidth = 1800;
        public const double DefaultCanvasHeight = 1100;

        public FlowViewState()
        {
            Zoom = 1.0;
            CanvasWidth = DefaultCanvasWidth;
            CanvasHeight = DefaultCanvasHeight;
            Nodes = new Dictionary<string, NodeViewState>();
        }

        public double Zoom { get; set; }

        public double OffsetX { get; set; }

        public double OffsetY { get; set; }

        /// <summary>
        /// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝศฃ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ `.flowdesign` ๏ฟฝ๏ฟฝอผืดฬฌ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฬฌสฑ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฦณ๏ฟฝ๏ฟฝ๏ฟฝ
        /// </summary>
        public double CanvasWidth { get; set; }

        /// <summary>
        /// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ฿ถศฃ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ `.flowdesign` ๏ฟฝ๏ฟฝอผืดฬฌ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฬฌสฑ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฦณ๏ฟฝ๏ฟฝ๏ฟฝ
        /// </summary>
        public double CanvasHeight { get; set; }

        public Dictionary<string, NodeViewState> Nodes { get; set; }
    }
}
