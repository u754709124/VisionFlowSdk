namespace Vision.Flow.Core.Domain.Flows
{
    /// <summary>
    /// ๏ฟฝ๏ฟฝ๏ฟฝฬฌ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤต๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝษท๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฬฌ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝอผืดฬฌ๏ฟฝ๏ฟฝ
    /// </summary>
    public sealed class FlowDesignDocument
    {
        public FlowDesignDocument()
        {
            SchemaVersion = 1;
            Runtime = new RuntimeFlowDefinition();
            View = new FlowViewState();
        }

        /// <summary>
        /// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฮจาป๏ฟฝ๏ฟฝสถ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤผ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤผ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝยผ๏ฟฝึฎ๏ฟฝไฝจ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
        /// </summary>
        public string FlowId { get; set; }

        /// <summary>
        /// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝหต๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฦฃ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝึด๏ฟฝะต๏ฟฝ๏ฟฝศก๏ฟฝ
        /// </summary>
        public string FlowName { get; set; }

        /// <summary>
        /// ๏ฟฝฤผ๏ฟฝ๏ฟฝแนน๏ฟฝๆฑพ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฺบ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
        /// </summary>
        public int SchemaVersion { get; set; }

        /// <summary>
        /// ๏ฟฝษท๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฬฌ๏ฟฝ๏ฟฝ๏ฟฝๅกฃ
        /// </summary>
        public RuntimeFlowDefinition Runtime { get; set; }

        /// <summary>
        /// ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝสน๏ฟฝรตฤป๏ฟฝ๏ฟฝ๏ฟฝืดฬฌ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ `.flowruntime` สฑ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฦณ๏ฟฝ๏ฟฝ๏ฟฝ
        /// </summary>
        public FlowViewState View { get; set; }
    }
}
